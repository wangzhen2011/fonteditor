package com.keitaitoys.cocos2dfntfontplugin;

import com.keitaitoys.util.ExtensionFileFilter;

public class Cocos2dFNTFileFilter extends ExtensionFileFilter {

	//////////////////////////////////////////////////////////////////////
	// Description ///////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////
	// Consts ////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	private static final String[] EXTENSIONS =  new String[] {
		
		"FNT"
	};
	
	//////////////////////////////////////////////////////////////////////
	// Variables /////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////
	// Constructor ///////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////
                             
    public Cocos2dFNTFileFilter() {
    	
    	extensions = EXTENSIONS;
    }

	//////////////////////////////////////////////////////////////////////
	// Возвращает описание фильтра
	//////////////////////////////////////////////////////////////////////

    public String getDescription() {

    	return "FNT (*.FNT)";
    }

	//////////////////////////////////////////////////////////////////////
	// Возвращает строку разрешения
	//////////////////////////////////////////////////////////////////////

    public String getExtension() {

    	return "fnt";
    }
}
