package com.keitaitoys.fonteditor.gui;

import java.awt.event.ActionEvent;

import javax.swing.AbstractAction;
import javax.swing.text.JTextComponent;

@SuppressWarnings("serial")
public class TextComponentDecreaseIntegerValueAction extends AbstractAction {

	//////////////////////////////////////////////////////////////////////
	// Description ///////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////
	// Consts ////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////
	// Variables /////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	private JTextComponent textComponent;
	
	//////////////////////////////////////////////////////////////////////
	// Constructor ///////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public TextComponentDecreaseIntegerValueAction(JTextComponent textComponent) {
		
		this.textComponent = textComponent;
	}

	//////////////////////////////////////////////////////////////////////
	// Functions /////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public void actionPerformed(ActionEvent event) {

		if(textComponent != null) {
			
			try {
				
				int value = Integer.parseInt(textComponent.getText());
				value--;
				textComponent.setText(String.valueOf(value));

			} catch(NumberFormatException e) {}
		}
	}
}
