package com.keitaitoys.fonteditor.gui;

import java.awt.Color;

public class ColorThumbModel {

	//////////////////////////////////////////////////////////////////////
	// Description ///////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////
	// Consts ////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////
	// Variables /////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	private Color color;
	
	//////////////////////////////////////////////////////////////////////
	// Constructor ///////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public ColorThumbModel() {
		
		this(new Color(0, 0, 0));
	}

	//////////////////////////////////////////////////////////////////////
	// Constructor ///////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public ColorThumbModel(Color color) {
		
		setColor(color);
	}

	//////////////////////////////////////////////////////////////////////
	// Function //////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public Color getColor() {
		
		return color;
	}

	//////////////////////////////////////////////////////////////////////
	// Function //////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public void setColor(Color color) {
		
		this.color = color;
	}
}