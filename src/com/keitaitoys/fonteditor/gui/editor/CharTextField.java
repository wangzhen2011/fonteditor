package com.keitaitoys.fonteditor.gui.editor;

import java.awt.event.KeyEvent;

import javax.swing.Action;
import javax.swing.JComponent;
import javax.swing.JTextField;
import javax.swing.KeyStroke;
import javax.swing.event.DocumentEvent;
import javax.swing.event.DocumentListener;

import com.keitaitoys.fonteditor.core.Manager;
import com.keitaitoys.fonteditor.gui.TextComponentDecreaseCharValueAction;
import com.keitaitoys.fonteditor.gui.TextComponentIncreaseCharValueAction;

public class CharTextField extends JTextField implements DocumentListener {

	//////////////////////////////////////////////////////////////////////
	// Description ///////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////
	// Consts ////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////
	// Variables /////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////
	// Constructor ///////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public CharTextField() {
		
		Manager manager = Manager.getInstance();
		manager.registerCharTextField(this);

		init();
	}

	//////////////////////////////////////////////////////////////////////
	// Functions /////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	private void init() {
		
		setEnabled(true);
		setColumns(6);

		Action textComponentIncreaseCharValueAction = new TextComponentIncreaseCharValueAction(this);
		Action textComponentDecreaseCharValueAction = new TextComponentDecreaseCharValueAction(this);
		
		getInputMap(JComponent.WHEN_FOCUSED).put(KeyStroke.getKeyStroke(KeyEvent.VK_UP, 0), "textComponentIncreaseCharValueAction");
		getInputMap(JComponent.WHEN_FOCUSED).put(KeyStroke.getKeyStroke(KeyEvent.VK_DOWN, 0), "textComponentDecreaseCharValueAction");

		getActionMap().put("textComponentIncreaseCharValueAction", textComponentIncreaseCharValueAction);
		getActionMap().put("textComponentDecreaseCharValueAction", textComponentDecreaseCharValueAction);

		getDocument().addDocumentListener(this);
	}

	//////////////////////////////////////////////////////////////////////
	// Functions /////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public void changedUpdate(DocumentEvent e) {

	}
	
	//////////////////////////////////////////////////////////////////////
	// Functions /////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public void insertUpdate(DocumentEvent e) {

	}

	//////////////////////////////////////////////////////////////////////
	// Functions /////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////

	public void removeUpdate(DocumentEvent e) {

	}
}